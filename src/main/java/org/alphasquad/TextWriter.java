    final FileOutputStream fos;
    static long count = 0;
    String buffer = "";
    int lineCount = 0;
    final int bufferMaxLines = 100;
    final int TOTAL_LINES = 1000000000;


    public TextWriter(FileOutputStream fileOStream) {
        //constructor
        fos = fileOStream;
        //System.out.println("FOS IS: " + fos);
    }

    @Override
    public void run() {
        //run in a loop
        System.out.println("........Starting Thread........");

        while(true){
            //one at a time, ladies:
            double random = Math.random() * 45.00;
            DecimalFormat df = new DecimalFormat("#.##");
            String formattedRandom = df.format(random);

            Random rand = new Random();
            int randomIndex = rand.nextInt(cities.length);
            String randomLine = cities[randomIndex] + ":" + formattedRandom + "\n";

            if(lineCount < bufferMaxLines){
                buffer += randomLine;
                lineCount++;
                continue;
            }

            synchronized (fos) {
                if(count >= TOTAL_LINES){
                    System.out.println("........Stopping Thread........");
                    break;
                }
                try {
                    fos.write(buffer.getBytes());
//                    fos.flush();
                } catch (IOException e) {
                    System.out.println("-----------Error writing to file------------");
                }

                count += lineCount;
            }

            buffer = "";
            lineCount = 0;

            if(count >= TOTAL_LINES){
                System.out.println("........Stopping Thread........");
                break;
            }

        }//ewhile

    }//eof


